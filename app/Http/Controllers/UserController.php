    public function login(Request $request) {
        $dados = $request->all();
        $login = $this->user->login($dados);
        if(!$login) {
            return back()
                    ->withInput()
                    ->withErrors([
                        'As credenciais fornecidas nÃ£o correspondem aos nossos registros.'
                    ]);
        }
        return redirect()->intended(route('welcome'));
    }

